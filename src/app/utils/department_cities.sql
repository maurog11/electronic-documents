CREATE TRIGGER consecutive_payrollsendsinvoices
    BEFORE INSERT
    ON payrollsends
    FOR EACH ROW
BEGIN
    set @IssuerNit = New.IssuerNit; set @DocType = New.DocType; set @YearSend = New.YearSend; set @newconsecutive = 1;
    set @Id = (select Id
               from consecutivepayroll
               where IssuerNit = @IssuerNit
                 and DocType = @DocType
                 and YearSend = @YearSend for
               update);
    if @Id Is Null then
        insert into consecutivepayroll (CreateDate, UpdateDate, IssuerNit, DocType,
                                        YearSend, consecutive)
        values (New.CreateDate, New.UpdateDate, @IssuerNit, @DocType, @YearSend, 1);
    else
        set @newconsecutive = (select consecutive from consecutivepayroll where Id = @Id for update) + 1;
    update consecutivepayroll Set consecutive = @newconsecutive where Id = @Id;
end if;
set
New.NumberSend = @newconsecutive;

END;


